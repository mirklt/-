let currentLang = localStorage.getItem('sm_lang') || 'ru';

function t(key, vars) {
  let str = (I18N[currentLang] && I18N[currentLang][key]) || I18N.ru[key] || key;
  if (vars) Object.keys(vars).forEach(k => { str = str.replace(`{${k}}`, vars[k]); });
  return str;
}

function setLanguage(lang) {
  if (!I18N[lang]) return;
  currentLang = lang;
  localStorage.setItem('sm_lang', lang);
  document.documentElement.lang = lang === 'tg' ? 'tg' : lang === 'uz' ? 'uz' : lang;
  document.body.classList.toggle('lang-tg', lang === 'tg');
  applyTranslations();
  if (typeof renderAllPageContent === 'function') renderAllPageContent();
  renderProgressStrip();
  if (typeof phishActive !== 'undefined' && !phishActive) {
    const btn = document.getElementById('phish-start-btn');
    if (btn) btn.textContent = t('phish.start');
  }
  const phishPrompt = document.getElementById('phish-prompt');
  if (phishPrompt && !phishActive) phishPrompt.textContent = t('phish.prompt');
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const val = t(key);
    if (el.dataset.i18nHtml === 'true') el.innerHTML = val;
    else el.textContent = val;
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    el.setAttribute('aria-label', t(el.dataset.i18nAria));
  });
  document.querySelectorAll('option[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  const langSel = document.getElementById('lang-select');
  if (langSel) langSel.value = currentLang;
  const themeBtn = document.getElementById('theme-toggle');
  if (themeBtn) themeBtn.title = document.documentElement.dataset.theme === 'light' ? t('theme.dark') : t('theme.light');
}

function getEmergencyScenario(key) {
  const em = EMergencyI18N[currentLang]?.[key] || EMergencyI18N.ru[key];
  return {
    title: t(`em.${key}.title`),
    short: t(`em.${key}.short`),
    intro: em.intro,
    steps: em.steps,
    faq: key === 'sms' || key === 'transfer' || key === 'card' ? 'faq-chargeback' : 'faq-online',
    faqLabel: em.faqLabel
  };
}

function syncThemeAccent() {
  const light = document.documentElement.dataset.theme === 'light';
  if (window.__heroMeshMat) window.__heroMeshMat.color.setHex(light ? 0xc026d3 : 0x5888e8);
}

function toggleTheme() {
  const next = document.documentElement.dataset.theme === 'light' ? 'dark' : 'light';
  document.documentElement.dataset.theme = next;
  localStorage.setItem('sm_theme', next);
  const btn = document.getElementById('theme-toggle');
  if (btn) {
    btn.textContent = next === 'light' ? '🌙' : '☀️';
    btn.title = next === 'light' ? t('theme.dark') : t('theme.light');
  }
  if (typeof syncThemeAccent === 'function') syncThemeAccent();
}

function initTheme() {
  const saved = localStorage.getItem('sm_theme') || 'dark';
  document.documentElement.dataset.theme = saved;
  const btn = document.getElementById('theme-toggle');
  if (btn) {
    btn.textContent = saved === 'light' ? '🌙' : '☀️';
    btn.title = saved === 'light' ? t('theme.dark') : t('theme.light');
  }
}
